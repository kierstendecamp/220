import math


class Spheres:

    def __init__(self, radius):
        self.radius = radius
        self.area = 0
        self.volume = 0


def get_radius(self):
    return self.radius


def surface_area(self):
    rad = self.radius
    self.area = 4 * math.pi * (rad * rad)
    return self.area


def volume(self):
    rad = self.radius
    self.volume = (4/3) * math.pi * (rad * rad * rad)
    return self.volume






